export function filterHadiths(categoryId: string, search: string): Hadith[] {
  let list = hadiths;
  if (categoryId !== "all") list = list.filter((h) => h.category === categoryId);
  const q = search.trim();
  if (q) {
    list = list.filter(
      (h) => h.text.includes(q) || h.narrator.includes(q) || h.source.includes(q)
    );
  }
  return list;
}
